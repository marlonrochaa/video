    {
        $videos = Video::JOIN('users','users.id','=','videos.fk_usuario')
            ->SELECT('videos.id','users.name as nome','videos.texto','videos.mp4','videos.imagem')
            ->GET();

        return view('videos.index',compact('videos'));
